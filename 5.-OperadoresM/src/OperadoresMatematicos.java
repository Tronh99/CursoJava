
public class OperadoresMatematicos {
	//Atributos o Variables de Clase
	
	
	//Constructor
	public OperadoresMatematicos() {
		
	}
	
	//Metodo
	public void suma() {
		int numero1		= 5;
		int numero2		= 5;
		int respuesta	= numero1 + numero2;
		System.out.println("Respuesta de la suma = " + respuesta);
		
	}
	
	//Metodo
	public void resta() {

		int numero3		= 70;
		int numero4		= 1;
		int resultado		= numero3 - numero4;
		System.out.println("El resultado de la resta es = " + resultado);
	}
	
	//Metodo
	public void multiplicacion() {
		int numero5		= 5;
		int numero6 	= 9; 
		int res			= numero5 * numero6;
		System.out.println("El resultado de la multiplicacion es = " + res);
	}
}
